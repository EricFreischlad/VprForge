namespace VprModLib
{
    /// <summary>
    /// The type of track: Vocaloid or Audio.
    /// </summary>
    public enum TrackType
    {
        /// <summary>
        /// VOCALOID track. Represented as Type "0".
        /// </summary>
        VOCALOID = 0,
        /// <summary>
        /// Audio track. Represented as Type "1".
        /// </summary>
        AUDIO = 1,
    }
}